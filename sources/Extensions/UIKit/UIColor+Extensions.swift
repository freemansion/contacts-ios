import UIKit

extension UIColor {
    enum Theme {
        @nonobjc static var greenishCyan: UIColor {
            return UIColor(red: 80.0 / 255.0, green: 227.0 / 255.0, blue: 194.0 / 255.0, alpha: 1.0)
        }

        @nonobjc static var greyishBrown: UIColor {
            return UIColor(white: 74.0 / 255.0, alpha: 1.0)
        }
    }
}
